SECTION .DATA
	S_READ EQU 0
	S_WRITE EQU 1
	S_OPEN EQU 2
	S_CLOSE EQU 3
	S_EXIT EQU 60
	O_RDONLY EQU 0O0000_0000
	O_CREAT_WRONLY_TRUNC EQU 0O0000_1101
	F_STDIN EQU 0
	F_STDOUT EQU 1
	F_STDERR EQU 2
	EOF EQU 0
	FNAME DB "HEYNOW.TXT", 0
	MSG DB "HEY DIDDLE DIDDLE!", 0
	MSGLEN EQU 18 ; $ - MSG
SECTION .TEXT
GLOBAL _START
_START:
	MOV RAX, S_OPEN
	MOV RDI, FNAME
	MOV RSI, O_CREAT_WRONLY_TRUNC
	MOV RDX, 0O666
	SYSCALL
	MOV RDI, RAX
	MOV RAX, S_WRITE
	MOV RSI, MSG
	MOV RDX, MSGLEN
	SYSCALL
	MOV RDI, RAX
	MOV RAX, 60
	SYSCALL
